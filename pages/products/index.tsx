export async function getStaticProps() {
		const response = await fetch(`${STRAPI_API}/api/products`)

		if (!response.ok) {
			throw new Error(await response.text())
		}

		const products: ProductResponse = await response.json()
		return {
			props: {
				products: products.data,
			},
		}
	}
